let semana = 2;

switch (semana) {
case 1:
    {console.log(`Domingo! 😎`)};
break;
case 2:
    {console.log(`Segundou! 😑`)};
break;
case 3:
    {console.log(`Terça-feira`)};
break;
case 4:
    {console.log(`Quarta-feira`)};
break;
case 5:
    {console.log(`Quinta-feira`)};
break;
case 6:
    {console.log(`Sextou finalmente! 🎉`)};
break;
case 7:
    {console.log(`Sabadou! 🤪`)};
break;
default:
    {console.log(`Número inválido 🚨`)};
}

